export function normalizeLanguage(value: unknown): Language {
  return value === "en" || value === "zh" ? value : DEFAULT_LANGUAGE;
}

export function translate(language: Language, key: TranslationKey, values: Record<string, string | number> = {}): string {
  let text: string = translations[language][key] ?? translations[DEFAULT_LANGUAGE][key];
  for (const [name, value] of Object.entries(values)) {
    text = text.split(`{${name}}`).join(String(value));
  }
  return text;
}
